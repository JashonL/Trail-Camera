package com.shuoxd.camera.bean;

public class PhaseBean {
    private boolean selected;
    private int index;
    private int resSelected;
    private int resNormal;

    public int getResSelected() {
        return resSelected;
    }

    public void setResSelected(int resSelected) {
        this.resSelected = resSelected;
    }

    public int getResNormal() {
        return resNormal;
    }

    public void setResNormal(int resNormal) {
        this.resNormal = resNormal;
    }

    public boolean isSelected() {
        return selected;
    }

    public void setSelected(boolean selected) {
        this.selected = selected;
    }

    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }

}
